package com.lvmama.passport.processor.impl.client.ylgl.model;

public class Product {

	private String productId;// 产品编号
	private String productName;// 产品名称
	private String productFaceValue;// 产品对外报价
	private String productPlatformValue;// 产品销售价格
	private String productSalePrice;// 产品建议销售价格
	private String productTickettype;// 电子或机打票
	private String productTicketExplain;// 票面说明
	private String provideTel;// 资源方电话
	private String provideAddress;// 资源方地址
	private String productEndTime;// 失效日期

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getProductFaceValue() {
		return productFaceValue;
	}

	public void setProductFaceValue(String productFaceValue) {
		this.productFaceValue = productFaceValue;
	}

	public String getProductPlatformValue() {
		return productPlatformValue;
	}

	public void setProductPlatformValue(String productPlatformValue) {
		this.productPlatformValue = productPlatformValue;
	}

	public String getProductSalePrice() {
		return productSalePrice;
	}

	public void setProductSalePrice(String productSalePrice) {
		this.productSalePrice = productSalePrice;
	}

	public String getProductTickettype() {
		return productTickettype;
	}

	public void setProductTickettype(String productTickettype) {
		this.productTickettype = productTickettype;
	}

	public String getProductTicketExplain() {
		return productTicketExplain;
	}

	public void setProductTicketExplain(String productTicketExplain) {
		this.productTicketExplain = productTicketExplain;
	}

	public String getProvideTel() {
		return provideTel;
	}

	public void setProvideTel(String provideTel) {
		this.provideTel = provideTel;
	}

	public String getProvideAddress() {
		return provideAddress;
	}

	public void setProvideAddress(String provideAddress) {
		this.provideAddress = provideAddress;
	}

	public String getProductEndTime() {
		return productEndTime;
	}

	public void setProductEndTime(String productEndTime) {
		this.productEndTime = productEndTime;
	}

}
